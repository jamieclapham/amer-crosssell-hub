function computeQuarter(offset = 0) {
  const d = new Date();
  d.setDate(1);
  d.setMonth(d.getMonth() + offset * 3);
  const y = d.getFullYear(), m = d.getMonth() + 1;
  if (m <= 3)  return { label: `Q1 FY${y}`, start: `${y}-01-01`, end: `${y}-03-31` };
  if (m <= 6)  return { label: `Q2 FY${y}`, start: `${y}-04-01`, end: `${y}-06-30` };
  if (m <= 9)  return { label: `Q3 FY${y}`, start: `${y}-07-01`, end: `${y}-09-30` };
  return               { label: `Q4 FY${y}`, start: `${y}-10-01`, end: `${y}-12-31` };
}

